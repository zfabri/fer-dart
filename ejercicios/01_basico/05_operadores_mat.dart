/**
 * un operador es un simbolo que le dice al compilador
 * que debe realizar una tarea
 * matematica, relacionandola o logica
 * y que debe producir un resultado
 */

main() {
  int a = 10 + 5; // +
  a = 20 - 10; // -
  a = 10 * 2; // *

  double b = 10 / 2; // /
  b = 10 % 3; // %
  b = -b; // -expresion

  int c = 10 ~/ 3; // ~/ 

  int d = 1;
  d++; // ++ 2
  d--; // -- 0

  d += 2; // +=
  d -= 2; // -=
}
